package com.destination.septemberBatch.sep20;

import java.util.Scanner;

public class ScannerExample {
 public static void main(String[] args) {
	System.out.println("===Example of Dynamic Programming======");
	Scanner sc = new Scanner(System.in);
	System.out.println("enter the byte value");
	byte b =sc.nextByte();
	System.out.println("Byte value is :"+(b));
	System.out.println("+++++++++++++++++");
	Scanner sc1 = new Scanner(System.in);
	System.out.println("enter the short value");
	short s =sc1.nextShort();
	System.out.println("Short value is :"+(s));
	System.out.println("+++++++++++++++++");
	Scanner sc2 = new Scanner(System.in);
	System.out.println("enter the int value");
	int i =sc2.nextInt();
	System.out.println("Int value is :"+(i));
	System.out.println("+++++++++++++++++");
	Scanner sc3 = new Scanner(System.in);
	System.out.println("enter the long value");
	long l=sc3.nextLong();
	System.out.println("Long value is :"+(l));
	System.out.println("+++++++++++++++++");
	Scanner sc4 = new Scanner(System.in);
	System.out.println("enter the double value");
	double d =sc4.nextDouble();
	System.out.println("Double value is :"+(d));
	System.out.println("+++++++++++++++++");
	Scanner sc5 = new Scanner(System.in);
	System.out.println("enter the float value");
	float f =sc5.nextFloat();
	System.out.println("Float value is :"+(f));
	System.out.println("+++++++++++++++++");
	Scanner sc6 = new Scanner(System.in);
	System.out.println("enter the string value");
	String str1 =sc6.next();
	System.out.println("String value is :"+(str1));
	System.out.println("+++++++++++++++++");
	Scanner sc7 = new Scanner(System.in);
	System.out.println("enter the statement value");
	String str2 =sc7.nextLine();
	System.out.println("String Statement value is :"+(str2));
	System.out.println("+++++++++++++++++");
	
	
}
}
